def Relational_operators(num1, num2):

    print(f"{num1} > {num2} ",num1>num2)
    print(f"{num1} >= {num2} ",num1>=num2)
    print(f"{num1} < {num2} ",num1<num2)
    print(f"{num1} <= {num2} ",num1<=num2)

number1 = int(input("Enter first number "))
number2 = int(input("Enter second number "))

Relational_operators(number1, number2)

